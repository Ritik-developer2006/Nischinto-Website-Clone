    <h1 class="mt-5">When are medical x-rays used?</h1>
    <hr>
    <div class="pe-2">
      <div>Listed below are examples of examinations and procedures that use x-ray technology to either diagnose or
        treat disease:</div>
      <h4 class="mt-4">Diagnostic</h4>
      <ul>
        <li><span class="fw-bold text-dark text-decoration-underline">X-ray radiography: </span> Detects bone fractures,
          certain tumors and other abnormal masses, pneumonia, some types of injuries, calcifications, foreign objects,
          or dental problems.</li>
        <li><span class="fw-bold text-dark text-decoration-underline">Mammography:</span> A radiograph of the breast
          that is used for cancer detection and diagnosis. Tumors tend to appear as regular or irregular-shaped masses
          that are somewhat brighter than the background on the radiograph (i.e., whiter on a black background or
          blacker on a white background). Mammograms can also detect tiny bits of calcium, called microcalcifications,
          which show up as very bright specks on a mammogram. While usually benign, specific patterns of
          microcalcifications could indicate the presence of cancer. Learn more about mammography here.</li>
        <li><span class="fw-bold text-dark text-decoration-underline">Computed tomography (CT): </span> Combines
          traditional x-ray technology with computer processing to generate a series of cross-sectional images of the
          body that can later be combined to form a three-dimensional x-ray image. CT images are more detailed than
          plain radiographs and give doctors the ability to view structures within the body from many different angles.
          Learn more about CT here.</li>
        <li><span class="fw-bold text-dark text-decoration-underline">Fluoroscopy: </span> Uses x-rays and a fluorescent
          screen to obtain real-time images of movement within the body or to view diagnostic processes, such as
          following the path of an injected or swallowed contrast agent. For example, fluoroscopy is used to view the
          movement of the beating heart, and, with the aid of radiographic contrast agents, to view blood flow to the
          heart muscle as well as through blood vessels and organs. This technology is also used with a radiographic
          contrast agent to guide an internally threaded catheter during cardiac angioplasty, which is a minimally
          invasive procedure for opening clogged arteries that supply blood to the heart.</li>
      </ul>
      <h4 class="mt-4">Therapeutic</h4>
      <ul>
        <li>Radiation therapy in cancer treatment: X-rays and other types of high-energy radiation can be used to
          destroy cancerous tumors and cells by damaging their DNA. The radiation dose used for treating cancer is much
          higher than the radiation dose used for diagnostic imaging. Therapeutic radiation can come from a machine
          outside of the body or from a radioactive material that is placed in the body, inside or near tumor cells, or
          injected into the blood stream. Learn more about radiation treatment for cancer therapy here.</li>
      </ul>
