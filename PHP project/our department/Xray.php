    <h1 class="mt-5">How do medical x-ray work?</h1>
    <hr>
    <div class="row d-flex justify-content-around">
      <div class="col-xxl-8 col-xl-8 col-lg-8 col-12">
        To create a radiograph, a patient is positioned so that the part of the body being imaged is located between an
        x-ray source and an x-ray detector. When the machine is turned on, x-rays travel through the body and are
        absorbed in different amounts by different tissues, depending on the radiological density of the tissues they
        pass through. Radiological density is determined by both the density and the atomic number (the number of
        protons in an atom’s nucleus) of the material being imaged. For example, our bones contain calcium, which has a
        higher atomic number than most other tissues. Because of this property, bones readily absorb x-rays and
        therefore produce high contrast on the x-ray detector. As a result, bony structures appear whiter than other
        tissues against the black background of a radiograph. Conversely, x-rays travel more easily through less
        radiologically dense tissues, such as fat, muscle, and air-filled cavities such as the lungs. These structures
        are displayed in shades of gray on a radiograph.
