      <h1>What are NIBIB-funded researchers developing in the field of x-ray technology?
      </h1>
      <hr>
      <div>
        Current research of x-ray technology focuses on ways to reduce radiation dose, improve image resolution, and
        enhance contrast materials and methods. The following are examples of research projects funded by NIBIB that are
        developing new applications of x-ray technology:<br><br>

        X-ray of a broken arm
        Single-frame x-ray tomosynthesis (SFXT): Conventional x-ray radiography generates a single two-dimensional
        image, which is created by imaging a single plane at a single time point. X-ray tomosynthesis, on the other
        hand, uses multiple images, which are then reconstructed to generate more information, such as a
        three-dimensional image. Unlike CT imaging, where the source/detector physically travels at least 180 degrees
        around the patient, tomosynthesis uses a limited rotational angle and takes fewer images (requiring less
        radiation and less expense). Current tomosynthesis approaches, however, generate a static snapshot of the tissue
        of interest and do not allow for real-time imaging.<br><br>

        NIBIB-funded researchers are working on a new x-ray method, called single-frame x-ray tomosynthesis (or SFXT),
        that would allow for real-time monitoring of a small area of tissue. By capturing 30 images every second, this
        technique would have 10 to 100 times the temporal resolution of conventional tomosynthesis, resulting in
        “sharper” images of tissues in motion (similar to using a faster shutter speed on a camera). The researchers
        plan to evaluate the use of SFXT in the detection of cardiovascular disease — by looking at calcium deposits in
        the coronary arteries — and to guide radiation treatment to precise locations in the lungs, which would enable
        safer ablation of lung tumors.<br><br>

        Imaging to guide lung biopsies: Lung cancer is the leading cause of cancer-related mortality in the United
        States, and analyzing lesions found in the lungs is a way to screen for the disease and to guide treatment. For
        a biopsy, one method to obtain lung tissue is through a bronchoscopy, where a thin tube is passed through the
        nose or mouth and guided into the lungs. However, obtaining tissues of interest remains difficult, as locating
        and visualizing such lesions is challenging. To overcome these limitations, researchers have developed a new,
        cost-effective chest x-ray tomosynthesis system that can generate high-resolution, real-time images of the
        lungs, which would allow for improved visualization during a transbrochial biopsy. In addition to being less
        expensive and easier to use than standard CT-based approaches, this x-ray technique is stationary and does not
        require any physical motion of the x-ray source or detector. Further, this method uses low doses of radiation,
        which would be beneficial for patients who require multiple biopsies. This x-ray system is currently being
        optimized for pre-clinical large animal evaluation.<br><br>
