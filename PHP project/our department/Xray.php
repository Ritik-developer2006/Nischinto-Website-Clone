      <h1>Are there risks?</h1>
      <hr>
      <div>
        When used appropriately, the diagnostic benefits of x-ray scans significantly outweigh the risks. X-ray scans
        can diagnose possibly life-threatening conditions such as blocked blood vessels, bone cancer, and infections.
        However, x-rays produce ionizing radiation—a form of radiation that has the potential to harm living tissue.
        This is a risk that increases with the number of exposures added up over the life of an individual. However, the
        risk of developing cancer from radiation exposure is generally small.<br><br>

        An x-ray in a pregnant woman poses no known risks to the baby if the area of the body being imaged isn’t the
        abdomen or pelvis. In general, if imaging of the abdomen and pelvis is needed, doctors prefer to use exams that
        do not use radiation, such as magnetic resonance imaging (MRI) or ultrasound. However, if neither of those can
        provide the answers needed, or there is an emergency or other time constraint, an x-ray may be an acceptable
        alternative imaging option.<br><br>

        Because children are more sensitive to ionizing radiation and have a longer life expectancy, they have a higher
        relative risk for developing cancer from such radiation compared with adults. Parents may want to ask the
        technologist or doctor if their machine settings have been adjusted for children.<br><br>

        Learn more about specific risks involved with CT and mammography.
