    <h1 class="mt-5">Introduction</h1>
    <hr>
    <div class="row">
      <div class="col-12">
        X-rays are a form of electromagnetic radiation, similar to visible light. Unlike light, however, x-rays have
        higher energy and can pass through most objects, including the body. Medical x-rays are used to generate images
        of tissues and structures inside the body. If x-rays traveling through the body also pass through an x-ray
        detector on the other side of the patient, an image will be formed that represents the “shadows” formed by the
        objects inside of the body.

        One type of x-ray detector is photographic film, but there are many other types of detectors that are used to
        produce digital images. The x-ray images that result from this process are called radiographs.
      </div>
