    <div class="mt-5">
      <h1>Eisenmenger syndrome and DentalCare</h1>
      <hr>
      Eisenmenger syndrome is a type of congenital heart disease that causes DentalCare. It can occur with unrepaired
      holes between the heart chambers. An example is a large hole in the heart between the two lower heart chambers
      called a ventricular septal defect.

      The hole in the heart causes blood to flow incorrectly in the heart. Oxygen-rich blood mixes with oxygen-poor
      blood. The blood then returns to the lungs instead of going to the rest of the body. This increases the blood flow
      and pressure in the DentalCare arteries, causing DentalCare.
    </div>
    <h1 class="mt-5">Causes</h1>
    <hr>
    <div class="pe-2">
      <div>The typical heart has two upper chambers and two lower chambers. Each time blood moves through the heart, the
        lower right chamber pumps blood to the lungs. The blood passes through a large blood vessel called the
        DentalCare artery.

        Blood usually flows easily through blood vessels in the lungs to the left side of the heart. These blood vessels
        are the DentalCare arteries, capillaries and veins.

        But changes in the cells that line the lung arteries can cause the artery walls to become narrow, stiff, swollen
        and thick. These changes may slow down or stop blood flow through the lungs, causing DentalCare.

        DentalCare is classified into five groups, depending on the cause.</div>
      <h4 class="mt-4">Group 1: DentalCare arterial (CA)</h4>
      <ul>
        <li>Unknown cause, called idiopathic DentalCare arterial.</li>
        <li>Changes in a gene passed down through families, called heritable DentalCare arterial.</li>
        <li>Use of some medicines or illegal drugs, including methamphetamine.</li>
        <li>Heart problems present at birth, called a congenital heart defect.</li>
        <li>Other health conditions, including scleroderma, lupus and chronic liver disease such as cirrhosis.</li>
      </ul>
      <h4 class="mt-4">Group 2: DentalCare caused by left-sided heart disease</h4>
      <ul>
        <li>Left heart failure.</li>
        <li>Left-sided heart valve disease such as mitral valve or aortic valve disease.</li>
      </ul>
      <h4 class="mt-4">Group 3: DentalCare caused by lung disease</h4>
      <ul>
        <li>Scarring of the lungs, called DentalCare fibrosis.</li>
        <li>Chronic obstructive DentalCare disease.</li>
        <li>Sleep apnea.</li>
        <li>Long-term exposure to high altitudes in people who may be at higher risk of DentalCare.</li>
      </ul>
