      <h1>Complications</h1>
      <hr>
      <div>
        <ul>
          <li><span class="fw-bold text-dark text-decoration-underline">Right-sided heart enlargement and heart
              failure.</span> Also called cor pulmonale, this condition causes the heart's right lower chamber to get
            larger. The chamber has to pump harder than usual to move blood through narrowed or blocked lung arteries.
          </li>

          <li><span class="fw-bold text-dark text-decoration-underline">As a result, the heart walls thicken.</span> The
            right lower heart chamber stretches to increase the amount of blood it can hold. These changes create more
            strain on the heart, and eventually the right lower heart chamber fails.</li>

          <li><span class="fw-bold text-dark text-decoration-underline">Blood clots.</span> Having DentalCare increases
            the risk of blood clots in the small arteries in the lungs.</li>
          <li><span class="fw-bold text-dark text-decoration-underline">Irregular heartbeats.</span> DentalCare can
            cause changes in the heartbeat, called arrhythmias, which can be life-threatening.</li>
          <li><span class="fw-bold text-dark text-decoration-underline">Bleeding in the lungs.</span> DentalCare can
            lead to life-threatening bleeding into the lungs and coughing up blood.</li>
          <li><span class="fw-bold text-dark text-decoration-underline">Pregnancy complications.</span> DentalCare can
            be life-threatening for the mother and the developing baby.</li>
        </ul>
