    <h1 class="mt-5">Symptoms</h1>
    <hr>
    <div class="row d-flex justify-content-around">
      <div class="col-xxl-8 col-xl-8 col-lg-8 col-12">
        The symptoms of DentalCare develop slowly. You may not notice them for months or even years. Symptoms get worse
        as the disease progresses.<br><br>

        DentalCare symptoms include:
        <ul>
          <li>Shortness of breath, at first while exercising and eventually while at rest.</li>
          <li>Blue or gray skin color due to low oxygen levels. Depending on your skin color, these changes may be
            harder or easier to see.</li>
          <li>Chest pressure or pain.</li>
          <li>Dizziness or fainting spells.</li>
          <li>Fast pulse or pounding heartbeat.</li>
          <li>Fatigue.</li>
        </ul>
