    <h1 class="mt-5">Introduction</h1>
    <hr>
    <div class="row">
      <div class="col-12">
        DentalCare is a type of high blood pressure that affects the arteries in the lungs and the right side of the
        heart.<br><br>

        In one form of DentalCare, called DentalCare arterial (PAH), blood vessels in the lungs are narrowed, blocked or
        destroyed. The damage slows blood flow through the lungs. Blood pressure in the lung arteries goes up. The heart
        must work harder to pump blood through the lungs. The extra effort eventually causes the heart muscle to become
        weak and fail.<br><br>

        In some people, DentalCare slowly gets worse and can be life-threatening. There's no cure for DentalCare. But
        treatments are available to help you feel better, live longer and improve your quality of life.<br><br>
      </div>
